using System;

namespace Omprov2
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Ange kommando: ");
            string kommando = Console.ReadLine();
            string[] list = kommando.Split("-");


            for (int i = 0; i < list.Length; i++)
            {
                int tal = int.Parse(kommando);
                for (int j = 0; j < tal; j++)
                {
                    Console.Write("O");
                    Console.WriteLine();
                }
                
            }
        }
    }
}
